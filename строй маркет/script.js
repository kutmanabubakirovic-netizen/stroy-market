// --- 4. ФУНКЦИЯ ОТРИСОВКИ КАРТОЧЕК ---
function renderCatalog(products) {
    catalog.innerHTML = ''; 

    if (products.length === 0) {
        catalog.innerHTML = `<p style="grid-column: 1/-1; text-align: center;">Ничего не нашли... 🔍</p>`;
        return;
    }

    products.forEach(product => {
        const card = document.createElement('div');
        card.classList.add('product-card');
        const imagePath = `img/products/${product.image_id}.webp`;

        card.innerHTML = `
            <img src="${imagePath}" alt="${product.name}" onerror="this.src='https://placehold.co/200x160?text=Нет+фото'">
            <h3>${product.name}</h3>
            <p class="desc">${product.desc}</p>
            <div class="product-meta">
                <span class="product-price">${product.price} ₽</span>
                <button onclick="addToCart(this)">В корзину</button>
            </div>
        `;
        catalog.appendChild(card);
    });
}

// --- 5. ГЛАВНАЯ ФУНКЦИЯ ФИЛЬТРАЦИИ ---
function updateCatalog() {
    let filtered = [...productsData];

    // Фильтр по поиску
    const term = searchInput.value.toLowerCase();
    if (term) {
        filtered = filtered.filter(p => p.name.toLowerCase().includes(term));
    }

    // Фильтр по цене
    if (priceFilter) {
        const maxPrice = parseInt(priceFilter.value);
        filtered = filtered.filter(p => p.price <= maxPrice);
        if (priceValLabel) priceValLabel.innerText = maxPrice + " ₽";
    }

    // Фильтр по категориям
    if (activeCategory !== 'all') {
        filtered = filtered.filter(p => {
            const content = (p.name + p.desc).toLowerCase();
            return content.includes(activeCategory.toLowerCase().substring(0, 4));
        });
    }

    // Сортировка
    if (sortSelect.value === 'cheap') filtered.sort((a, b) => a.price - b.price);
    if (sortSelect.value === 'expensive') filtered.sort((a, b) => b.price - a.price);

    renderCatalog(filtered);
}

// --- 6. ЗАПУСК И СЛУШАТЕЛИ ---
if (searchInput) searchInput.addEventListener('input', updateCatalog);
if (priceFilter) priceFilter.addEventListener('input', updateCatalog);
if (sortSelect) sortSelect.addEventListener('change', updateCatalog);

catButtons.forEach(btn => {
    btn.addEventListener('click', () => {
        catButtons.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        activeCategory = btn.getAttribute('data-cat');
        updateCatalog();
    });
});

window.addToCart = function(btn) {
    btn.innerText = '✅ Ок';
    setTimeout(() => btn.innerText = 'В корзину', 1000);
};

// Первый запуск
renderCatalog(productsData);
